// loose comparisons -- > does not checks the datatype of the variable

console.log('Loose comparisons starts here !');
let age = 25;
console.log(age == 25);
console.log(age == '25');
console.log(age != 25);
console.log(age != '25');

// Strict comparisons --> checks the datatype of the variable


console.log('Strict comparison starts here !');
let likes = 20;
console.log(likes === 20);
console.log(likes === '20');
console.log(likes !== 20);
console.log(likes !== '20');

